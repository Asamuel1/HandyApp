class pageInfo extends StatelessWidget {
  final Color bgrColor;
  final String event;
  final String artist;
  final String genre;
  final String location;
  final String time;
  final String date;
  final String sponsor;
  final String feat;
  final String cost;
  final String description;
  final String youtubeUrl;
  final String mapUrl;

  pageInfo({
    //@required this.pg,
    @required this.bgrColor,
    @required this.event,
    @required this.artist,
    this.genre,
    @required this.location,
    @required this.time,
    @required this.date,
    @required this.sponsor,
    @required this.feat,
    @required this.cost,
    @required this.description,
    @required this.youtubeUrl,
    @required this.mapUrl,
  });

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        automaticallyImplyLeading: true,
        backgroundColor: bgrColor,
        title: Center(
          child: Text(event),
        ),
        leading: IconButton(
          //back button
          icon: Icon(Icons.arrow_back),
          onPressed: () => Navigator.pop(context, false),
        ),
      ),
      body: Container(
        padding: EdgeInsets.only(left: 20.0, right: 20.0, top: 20.0),
        child: Column(
          mainAxisAlignment: MainAxisAlignment.spaceEvenly,
          children: <Widget>[
            new AutoSizeText("Performer: " + artist, textAlign: TextAlign.left),
            new AutoSizeText("Genre: " + genre, textAlign: TextAlign.left),
            new AutoSizeText("Location: " + location,
                textAlign: TextAlign.left),
            new AutoSizeText("Date: " + date, textAlign: TextAlign.left),
            new AutoSizeText("Time: " + time, textAlign: TextAlign.left),
            new AutoSizeText("Sponsor: " + sponsor, textAlign: TextAlign.left),
            new AutoSizeText("Cost: " + cost, textAlign: TextAlign.left),
            new AutoSizeText("Featuring: " + feat,
                overflow: TextOverflow.visible, textAlign: TextAlign.left),
            new Flexible(
              //children: <Widget>[
              child: Container(
                padding: new EdgeInsets.only(right: 13.0),
                child: AutoSizeText("Description: " + description,
                    overflow: TextOverflow.visible, textAlign: TextAlign.left),
                //    ],
              ),
            ),
            Row(
              children: <Widget>[
                FlatButton(
                  onPressed: () {
                    Navigator.of(context).push(
                      MaterialPageRoute(
                        builder: (BuildContext context) => HandyWebView(
                          title: artist,
                          selectedUrl: youtubeUrl,
                          bgrColor: bgrColor,
                        ),
                      ),
                    );
                  },
                  child: Icon(Icons.library_music),
                ),
                FlatButton(
                  onPressed: () {
                    Navigator.of(context).push(
                      MaterialPageRoute(
                        builder: (BuildContext context) => HandyWebView(
                          title: "Map",
                          selectedUrl: mapUrl,
                          bgrColor: bgrColor,
                        ),
                      ),
                    );
                  },
                  child: Icon(Icons.map),
                ),
                FlatButton(
                  //favorite needs a function that collects fav data
                  onPressed: () {
                    Navigator.of(context).push(
                      MaterialPageRoute(
                        builder: (BuildContext context) => HandyWebView(
                          title: "Favorite",
                          selectedUrl: mapUrl,
                          bgrColor: bgrColor,
                        ),
                      ),
                    );
                  },
                  child: Icon(Icons.favorite_border),
                ),
              ],
            ),
          ],
        ),
      ),
    );
  }
}
